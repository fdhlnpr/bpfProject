                <script>
                    let cart = {};
                    let totalItems = 0;
                    let totalPrice = 0;
            
                    function addToCart(name, price) {
                        if (!cart[name]) {
                            cart[name] = { quantity: 0, price: price };
                        }
                        cart[name].quantity++;
                        totalItems++;
                        totalPrice += price;
                        updateCart();
                    }
            
                    function updateCart() {
                        const cartItemsElement = document.getElementById('cart-items');
                        cartItemsElement.innerHTML = '';
            
                        for (const [name, item] of Object.entries(cart)) {
                            const listItem = document.createElement('li');
                            listItem.innerHTML = `${name} - ${item.quantity} x Rp${item.price.toLocaleString('id-ID')}
                                <button onclick="addToCart('${name}', ${item.price})">+</button>
                                <button onclick="removeFromCart('${name}')">-</button>`;
                            cartItemsElement.appendChild(listItem);
                        }
            
                        document.getElementById('total-items').innerText = totalItems;
                        document.getElementById('total-price').innerText = totalPrice.toLocaleString('id-ID');
                    }
            
                    function removeFromCart(name) {
                        if (cart[name] && cart[name].quantity > 0) {
                            cart[name].quantity--;
                            totalItems--;
                            totalPrice -= cart[name].price;
            
                            if (cart[name].quantity === 0) {
                                delete cart[name];
                            }
                            updateCart();
                        }
                    }
            
                    $('#pay-button').on('click', function() {
                        if (totalItems === 0) {
                            alert('Keranjang Anda kosong. Silakan tambahkan barang terlebih dahulu.');
                            return;
                        }
            
                        // Ambil data untuk transaksi
                        const transactionData = {
                            name: prompt("Masukkan nama Anda:"),
                            email: prompt("Masukkan email Anda:"),
                            phone: prompt("Masukkan nomor telepon Anda:"),
                            total_amount: totalPrice
                        };
            
                        // Kirim data ke server untuk membuat transaksi
                        $.ajax({
                            url: '/jasa/transaction',
                            method: 'POST',
             data: transactionData,
                            success: function(response) {
                                // Buka halaman pembayaran Midtrans
                                snap.pay(response.token, {
                                    onSuccess: function(result) {
                                        alert('Pembayaran berhasil!');
                                        // Reset keranjang
                                        cart = {};
                                        totalItems = 0;
                                        totalPrice = 0;
                                        updateCart();
                                    },
                                    onError: function(result) {
                                        alert('Pembayaran gagal!');
                                    }
                                });
                            },
                            error: function(error) {
                                alert('Terjadi kesalahan saat membuat transaksi.');
                            }
                        });
                    });
            
                    $('.nav-articles').addClass('active');
                </script>
